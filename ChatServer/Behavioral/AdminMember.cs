namespace ChatServer.Behavioral
{
    public class AdminMember : TeamMember
    {
        public AdminMember(string name) : base(name)
        {
        }

        public override void Receive(string from, string message)
        {
            Console.Write($"{this.Name} ({nameof(AdminMember)}) has received: ");
            base.Receive(from, message);
        }
    }
}
